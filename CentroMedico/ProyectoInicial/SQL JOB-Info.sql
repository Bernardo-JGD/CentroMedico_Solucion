Un scheduled JOB está administrado por SQL Server Agent (es un componente dentro de SQL Server que administra
las tareas programadas).

Una tarea programada es un proceso en el que nosotros podemos crear y especificarle que realice ciertas acciones
que ejecute ciertos scripts en SQL Server y que lo ejecute en determinado momento y que lo pueda repetir, por ejemplo,
varias veces al día.

Los Scheduled JOB se utilizan para generar procesos que permitan procesar datos de tablas y que me sirvan para 
tomar desiciones más adelante, por ejemplo, para cambiarle el estado a algún registro en particular por algún motivo específico.


